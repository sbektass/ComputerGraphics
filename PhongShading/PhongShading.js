function prepareBear()
{
	
	var vertLength = vertices.length/3;
	indexedNormals = new Array(vertLength).fill(vec3(0,0,0));
	
	for(var i = 2; i < triangles.length; i+=3)
	{
		
		var normal = vec3(normals[(triangles[i]-1)*3], normals[((triangles[i]-1)*3)+1], normals[((triangles[i]-1)*3)+2]);
		
		if(indexedNormals[triangles[i-2]-1] == undefined)
			indexedNormals[triangles[i-2]-1]=normal;
		else
			indexedNormals[triangles[i-2]-1] = add(normal, indexedNormals[triangles[i-2]-1]);
	}
	
	for (var i = 0; i < triangles.length; i+=9)
	{	
		var vIndex1 = triangles[i]-1;
		var vIndex2 = triangles[i+3]-1;
		var vIndex3 = triangles[i+6]-1;
		
		meshVertices.push(vertices[3*vIndex1], vertices[3*vIndex1+1], vertices[3*vIndex1+2]);
		meshVertices.push(vertices[3*vIndex2], vertices[3*vIndex2+1], vertices[3*vIndex2+2]);
		meshVertices.push(vertices[3*vIndex3], vertices[3*vIndex3+1], vertices[3*vIndex3+2]);
		
		var tIndex1 = triangles[i+1]-1;
		var tIndex2 = triangles[i+4]-1;
		var tIndex3 = triangles[i+7]-1;
		
		meshTexCoords.push(textureCoords[2*tIndex1], textureCoords[2*tIndex1+1]);
		meshTexCoords.push(textureCoords[2*tIndex2], textureCoords[2*tIndex2+1]);
		meshTexCoords.push(textureCoords[2*tIndex3], textureCoords[2*tIndex3+1]);
		
		meshNormals.push(indexedNormals[vIndex1][0], indexedNormals[vIndex1][1], indexedNormals[vIndex1][2]);
		meshNormals.push(indexedNormals[vIndex2][0], indexedNormals[vIndex2][1], indexedNormals[vIndex2][2]);
		meshNormals.push(indexedNormals[vIndex3][0], indexedNormals[vIndex3][1], indexedNormals[vIndex3][2]);
	}
}

function render()
{
	gl.clear( gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);
	
	if(model_control==0)
		gl.drawArrays(gl.TRIANGLES,0,(meshVertices.length)/3);
	
    else 
    {
		for( var i=0; i<meshVertices.length; i+=3)
			gl.drawArrays( gl.LINE_LOOP, i,3 );
    }
	requestAnimationFrame(render);
}